# C I C L O S

# Ciclo while
i = 1
while i < 32:
	print(i)
	i += 1
else:
	print("i no es menor que 32")

# Ciclo for
sports = ["football", "tennis", "basketball", "baseball"]

for sp in sports:
	print(sp)

# Con rango
for n in range(4, 12):
	print(n)
print("\n")
# Con rango de 3 en 3
for n in range(3, 30, 3):
	print(n)
